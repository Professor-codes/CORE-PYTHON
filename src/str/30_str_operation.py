# concatenation
str1 = "Hello"
str2 = "World"
result = str1 + " " + str2  # Output: "Hello World"
print(result)

# repetition
str3 = "py"
result = str3 * 10  # Output: "pypypypypypypypypypy"
print(result)

# membership
str4 = "Hello, World!"
print("World" in str4)  # Output: True
print("Python" not in str4)  # Output: True
print("Python" in str4)  # Output: False
print("World" not in str4)  # Output: False

# slicing
str5 = "Hello, World!"
print(str5[0:5])   # Output: "Hello"
print(str5[5:8])
print(str5[7:12])  # Output: "World"

