# tuple indexing is the process of accessing elements of a tuple
# using their index values
# index starts from 0

my_tuple = (10, 20, 30, 40, 50)
# positive indexing
print(my_tuple[0])  # Output: 10
print(my_tuple[2])  # Output: 30
# negative indexing
print(my_tuple[-1])  # Output: 50
print(my_tuple[-3])  # Output: 30
